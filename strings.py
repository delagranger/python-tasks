def strings_task33():
    """
    Перед началом похудения у Гвидо был вес 100 кг, 
    а своей целью он поставил достичь веса 88 кг (или меньше). 
    Он решил худеть на одну и ту же массу ежедневно.
    Напишите программу, которая принимает на вход текущий день и текущий вес Гвидо. 
    Программа должна вывести фразу:
    «Все идет по плану» (без кавычек), если Гвидо удаётся держать планку в похудении 
    и его вес ниже либо равен тому, который он запланировал на текущий день
    «Что-то пошло не так» (без кавычек), если Гвидо не очень старается 
    и его вес выше того, который он запланировал на текущий день
    Также программа должна вывести информацию о номере дня похудения, 
    текущем весе Гвидо и цели по весу на текущий день.
    """

    lost_weight_per_day = 12 / 60
    day = int(input())
    curr_weight = float(input())
    needed_weight = 100 - day * lost_weight_per_day

    if curr_weight <= needed_weight:
        print("Все идет по плану")
    else:
        print("Что-то пошло не так")
    
    print(f"#{day} ДЕНЬ: ТЕКУЩИЙ ВЕС = {curr_weight} кг, ЦЕЛЬ по ВЕСУ = {needed_weight} кг")