def strings_task29():
    """
    Правила для корректного никнейма в соцсети Y следующие:
    - никнейм должен начинаться с символа @
    - никнейм должен содержать от 5 до 15 (включительно) символов (включая первый символ @)
    - никнейм должен содержать только строчные буквы и (или) цифры (помимо первого символа @)
    Напишите программу, которая выводит «Correct» (без кавычек), 
    если никнейм соответствует всем вышеприведенным правилам, 
    или «Incorrect» (без кавычек) в противном случае.
    """

    nick = input()
    if nick.startswith('@'):
        if 5 <= len(nick) <= 15:
            nick_new = nick[1:]
            if nick_new.isalnum():
                if nick_new.isdigit():
                    print("Correct")
                elif nick_new.islower():
                    print("Correct")
                else:
                    print("Incorrect")
            else:
                print("Incorrect")
        else:
            print("Incorrect")
    else:
        print("Incorrect")


